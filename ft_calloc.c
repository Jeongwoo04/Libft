#include "libft.h"

void	*ft_calloc(size_t cnt, size_t size)
{
	unsigned int	i;
	unsigned int	total;
	char			*dst;

	total = cnt * size;
	if (!(dst = malloc(total)))
		return (NULL);
	i = 0;
	while (total--)
	{
		dst[i] = 0;
		i++;
	}
	return ((void *)dst);
}
