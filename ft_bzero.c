#include "libft.h"

void				ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	char			*s_char;

	s_char = (char *)s;
	i = 0;
	while (i < n)
	{
		*(s_char + i) = 0;
		i++;
	}
}
